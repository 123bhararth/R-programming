# Assigning different types of values
double_var <- 3.14           # Double
integer_var <- 42L           # Integer
logical_var <- TRUE          # Logical (boolean)
complex_var <- 2 + 3i        # Complex number
char_var <- "Hello, World!"  # Character

# Displaying the type of each variable
print(paste("Type of double_var: ", class(double_var)))
print(paste("Type of integer_var: ", class(integer_var)))
print(paste("Type of logical_var: ", class(logical_var)))
print(paste("Type of complex_var: ", class(complex_var)))
print(paste("Type of char_var: ", class(char_var)))